# Live SSE streaming AI report endpoint
@app.post("/api/ai")
async def handle_ai_report(request: Request):
    try:
        body = await request.json()
        module = body.get("module") or "ca"
        query = body.get("query", "")
        
        api_key = os.getenv("OPENROUTER_API_KEY")
        if api_key:
            api_key = api_key.strip()
        model_name = os.getenv("OPENROUTER_MODEL", "anthropic/claude-3.5-sonnet")
        if model_name:
            model_name = model_name.strip()
        
        # Async stream generator for warning/mock loads
        async def mock_stream_generator(content: str):
            words = content.split(" ")
            for i, word in enumerate(words):
                chunk = word + (" " if i < len(words) - 1 else "")
                yield chunk
                delay = 0.045 if ("\n" in chunk or "." in chunk) else 0.012
                await asyncio.sleep(delay)

        # Sandbox mode detection - instruct user how to plug in live keys for real details
        if not api_key or "your-openrouter" in api_key or api_key.startswith("sk-ant-"):
            print(f"[FastAPI Sandbox] OpenRouter key missing or invalid. Mock streaming warning report for: {module}")
            
            # If the user specifically triggers the YoY Horizontal Statement comparison, run it cleanly
            if "comparison" in query.lower() or "uploaded" in query.lower() or "balance sheet" in query.lower():
                return StreamingResponse(mock_stream_generator(MOCK_COMPARISON_REPORT), media_type="text/plain")
                
            warning_msg = (
                "# Live API Connection Required\n"
                "**FinPro AI Platform Standing: Sandbox Active**\n\n"
                "---\n\n"
                "To generate actual, real-time professional reports, computations, and legal resolutions, "
                "you must configure a valid **OpenRouter API Key** inside your configuration file.\n\n"
                "### Next Steps to Connect Live Brain:\n"
                "1. Open the file [c:/projects/CA/backend/.env](file:///c:/projects/CA/backend/.env) in your editor.\n"
                "2. Replace `sk-or-v1-your-openrouter-api-key-configured-in-env` with your actual OpenRouter key (e.g. `sk-or-v1-abcde...`).\n"
                "3. Save the file. The FastAPI server will reload automatically.\n"
                "4. Resubmit your prompt! FinPro AI will immediately connect to OpenRouter (using model: `{model_name}`) to perform live, accurate financial intelligence calculations.\n\n"
                "---\n"
                "*Disclaimer: FinPro AI requires a valid live API connection to run real-world legal and cost audit worksheets.*"
            ).replace("{model_name}", model_name)
            return StreamingResponse(mock_stream_generator(warning_msg), media_type="text/plain")

        # Live Async OpenRouter API stream using httpx
        module_prompt = module_prompts.get(module.lower(), "")
        system_prompt = f"{MASTER_SYSTEM_PROMPT}\n\nACTIVE MODULE: {module.upper()}\n\n{module_prompt}"

        headers = {
            "Authorization": f"Bearer {api_key}",
            "Content-Type": "application/json",
            "HTTP-Referer": "http://localhost:8000",
            "X-Title": "FinPro AI"
        }
        
        # Read max_tokens from environment, default to 1000 to fit local credit thresholds
        max_tokens_str = os.getenv("OPENROUTER_MAX_TOKENS", "1000")
        try:
            max_tokens = int(max_tokens_str.strip())
        except Exception:
            max_tokens = 1000

        payload = {
            "model": model_name,
            "messages": [
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": query}
            ],
            "stream": True,
            "max_tokens": max_tokens
        }

        async def event_stream():
            try:
                async with httpx.AsyncClient() as client:
                    async with client.stream(
                        "POST",
                        "https://openrouter.ai/api/v1/chat/completions",
                        headers=headers,
                        json=payload,
                        timeout=60.0
                    ) as response:
                        if response.status_code != 200:
                            error_body = await response.aread()
                            print(f"OpenRouter Error status {response.status_code}: {error_body}")
                            yield f"Error from OpenRouter: Status {response.status_code}. Detail: {error_body.decode('utf-8')}"
                            return
                        
                        async for line in response.aiter_lines():
                            if line.startswith("data: "):
                                data_str = line[6:].strip()
                                if data_str == "[DONE]":
                                    break
                                try:
                                    data_json = json.loads(data_str)
                                    choices = data_json.get("choices", [])
                                    if choices:
                                        delta = choices[0].get("delta", {})
                                        content = delta.get("content", "")
                                        if content:
                                            yield content
                                except Exception:
                                    pass
            except Exception as e:
                print(f"OpenRouter streaming connection error: {e}")
                yield f"\n\n**Connection Error**: Failed to stream from OpenRouter. Please verify your internet connection and API key configuration.\nDetails: {str(e)}"

        return StreamingResponse(event_stream(), media_type="text/plain")

    except Exception as e:
        print(f"Server error during AI stream: {e}")
        raise HTTPException(status_code=500, detail=f"Internal server calculation fault: {str(e)}")

